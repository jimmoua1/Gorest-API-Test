package demo.Practice.Loops;

public class LoopsIncrementAndReverse {
    public static void main(String[] args) {
        // Reverse loop from 9 to 0
        for(int i = 9; i >= 0; i--){
            System.out.println(i);
        }

        // Reverse from 10 to 0
        for(int i = 10; i >= 0; i--){
            System.out.println(i);
        }
    }
}
